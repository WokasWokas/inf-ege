def foo(n):
    if n < 3:
        return n
    
    if n > 2 and n % 2 == 0:
        return 2 * (n - 1) + foo(n - 1) + 2
    else:
        return 2 * (n + 1) + foo(n - 2) - 5

print(foo(32))